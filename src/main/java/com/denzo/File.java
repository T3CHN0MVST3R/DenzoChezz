package com.denzo;

/**
 * Перечисление, представляющее файлы (колонки) на шахматной доске.
 */
public enum File {
    A, B, C, D, E, F, G, H;

    /**
     * Преобразует символ в соответствующий файл (File).
     *
     * <p>
     * Метод принимает символ, представляющий файл шахматной доски (например, 'a', 'B', 'h'),
     * и возвращает соответствующий элемент перечисления {@link File}. Если символ не соответствует
     * ни одному из файлов, метод возвращает {@code null}.
     * </p>
     *
     * @param c символ, представляющий файл (буква от 'A' до 'H', регистр не важен).
     * @return {@link File} соответствующий символу, или {@code null}, если символ невалиден.
     */
    public static File fromChar(char c) {
        try {
            // Преобразование символа в верхний регистр и получение соответствующего файла
            return File.valueOf(String.valueOf(c).toUpperCase());
        } catch (IllegalArgumentException e) {
            // Возврат null, если символ не соответствует ни одному из файлов
            return null;
        }
    }
}
